# Using % Operator
"""
%d – integer
%f – float
%s – string
%x – hexadecimal
%o – octal
"""
name = input("Enter your first name:")
age = int(input("What is your age:"))
per = float(input("Enter total percentage:"))
print("Your name is %s" % name)
print("Your age is %d" % age)
print("Your percentage is %s" % per)
